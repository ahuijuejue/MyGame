function GET_ABILITY_TEXT(_type)
    local text = ""
    if _type == 1 then
        text = GET_TEXT_DATA("TEXT_HP")
    elseif _type == 2 then
        text = GET_TEXT_DATA("TEXT_ATK")
    elseif _type == 3 then
        text = GET_TEXT_DATA("TEXT_M_ATK")
    elseif _type == 4 then
        text = GET_TEXT_DATA("TEXT_DEF")
    elseif _type == 5 then
        text = GET_TEXT_DATA("TEXT_M_DEF")
    elseif _type == 6 then
        text = GET_TEXT_DATA("TEXT_ACP")
    elseif _type == 7 then
        text = GET_TEXT_DATA("TEXT_M_ACP")
    elseif _type == 8 then
        text = GET_TEXT_DATA("TEXT_RATE")
    elseif _type == 9 then
        text = GET_TEXT_DATA("TEXT_DODGE")
    elseif _type == 10 then
        text = GET_TEXT_DATA("TEXT_CRIT")
    elseif _type == 11 then
        text = GET_TEXT_DATA("TEXT_BLOOD")
    elseif _type == 12 then
        text = GET_TEXT_DATA("TEXT_BREAKS")
    elseif _type == 13 then
        text = GET_TEXT_DATA("TEXT_TUMBLE")
    end
    return text
end

function GET_TEXT_COLOR(_type)
    if _type == 1 then
        return GET_TEXT_DATA("TEXT_WHITE")
    elseif _type == 2 then
        return GET_TEXT_DATA("TEXT_GREEN")
    elseif _type == 3 then
        return GET_TEXT_DATA("TEXT_BLUE")
    elseif _type == 4 then
        return GET_TEXT_DATA("TEXT_PURPLE")
    elseif _type == 5 then
        return GET_TEXT_DATA("TEXT_ORANGE")
    end
    return GET_TEXT_DATA("TEXT_WHITE")
end

function GET_TEXT_DATA(text)
    return TEXT_DATA[text][SETTING_LANGUAGE]
end